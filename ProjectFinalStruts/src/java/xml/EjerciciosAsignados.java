public class EjerciciosAsignados {
    int numeroEjercicio;
    String grupoCompleto;
    int numeroAlumno;
    int idGrupo;

    public int getIdGrupo() {
        return idGrupo;
    }

    public void setIdGrupo(int idGrupo) {
        this.idGrupo = idGrupo;
    }

    public EjerciciosAsignados() {
    }

    public int getNumeroEjercicio() {
        return numeroEjercicio;
    }

    public void setNumeroEjercicio(int numeroEjercicio) {
        this.numeroEjercicio = numeroEjercicio;
    }

    public String getGrupoCompleto() {
        return grupoCompleto;
    }

    public void setGrupoCompleto(String grupoCompleto) {
        this.grupoCompleto = grupoCompleto;
    }

    public int getNumeroAlumno() {
        return numeroAlumno;
    }

    public void setNumeroAlumno(int numeroAlumno) {
        this.numeroAlumno = numeroAlumno;
    }
}
